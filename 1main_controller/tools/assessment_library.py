import random

# ======================== 气态污染物等级判定 ========================


def _co2_level(co2: float) -> str:
    """CO2 等级判定（参考 GB/T 18883-2022 室内标准）"""
    if co2 <= 0:
        return ""
    elif co2 < 800:
        return "良好"
    elif co2 <= 1000:
        return "一般"
    elif co2 <= 1500:
        return "偏高"
    else:
        return "较高"


def _ch2o_level(ch2o: float) -> str:
    """甲醛等级判定（参考 GB/T 18883-2022: 1h 均值 ≤100μg/m³）"""
    if ch2o <= 0:
        return ""
    elif ch2o < 60:
        return "良好"
    elif ch2o <= 100:
        return "临界"
    else:
        return "超标"


def _tvoc_level(tvoc: float) -> str:
    """TVOC 等级判定（参考 GB/T 18883-2022: 8h 均值 ≤600μg/m³）"""
    if tvoc <= 0:
        return ""
    elif tvoc < 400:
        return "良好"
    elif tvoc <= 600:
        return "一般"
    elif tvoc <= 1000:
        return "偏高"
    else:
        return "较高"


