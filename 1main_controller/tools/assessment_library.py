# ======================== 对外接口 ========================

def get_assessment_text(comfort: str, aqi_key: str,
                        co2: float = 0, ch2o: float = 0, tvoc: float = 0) -> str:
    """根据舒适度描述和 AQI 大类，从语句库中随机返回一条综合评估文本。
    可选传入气态污染物数据，自动追加相关提示。

    Args:
        comfort: 舒适度描述，如 "舒适"、"偏热"、"寒冷不适" 等
        aqi_key:  AQI 归并后的大类，如 "优"、"良"、"轻度污染"、"中度污染"、"重度污染"
        co2:      eCO2 浓度 (ppm)，传 0 表示无数据
        ch2o:     eCH2O 浓度 (μg/m³)，传 0 表示无数据
        tvoc:     TVOC 浓度 (μg/m³)，传 0 表示无数据

    Returns:
        随机选取的评估语句（含气态污染物提示），如未找到精确匹配则尝试按 aqi_key 兜底，
        最终兜底返回通用提示。
    """
    key = (comfort, aqi_key)
    if key in ASSESSMENT_POOL:
        base = random.choice(ASSESSMENT_POOL[key])
    else:
        # 兜底：找不到精确匹配时，尝试仅按污染程度匹配
        base = None
        for (c, ak), sentences in ASSESSMENT_POOL.items():
            if ak == aqi_key:
                base = random.choice(sentences)
                break
