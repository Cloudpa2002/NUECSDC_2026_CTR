def _build_pollutant_notes(co2: float, ch2o: float, tvoc: float) -> str:
    """根据气态污染物数据生成追加提示（最多 2 条，按严重程度排序）"""
    warnings = []

    # 按健康影响严重程度排序：甲醛 > TVOC ≈ CO2
    cl = _ch2o_level(ch2o)
    if cl in ("超标", "临界"):
        key = ("ch2o", cl)
        if key in _POLLUTANT_NOTES:
            warnings.append(random.choice(_POLLUTANT_NOTES[key]))

    tl = _tvoc_level(tvoc)
    if tl in ("较高", "偏高"):
        key = ("tvoc", tl)
        if key in _POLLUTANT_NOTES:
            warnings.append(random.choice(_POLLUTANT_NOTES[key]))

    cl2 = _co2_level(co2)
    if cl2 in ("较高", "偏高"):
        key = ("co2", cl2)
        if key in _POLLUTANT_NOTES:
            warnings.append(random.choice(_POLLUTANT_NOTES[key]))

    # 最多返回 2 条，避免报告过长
    if warnings:
        return "；".join(warnings[:2])
    return ""


